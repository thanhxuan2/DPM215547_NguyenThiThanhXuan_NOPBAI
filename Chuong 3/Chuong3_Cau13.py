#13:Cho biết bao nhiêu dấu * được in ra trên màn hình


#Kết quả: chương trình sẽ in ra dấu * vô hạn không dừng.
a = 0
while a < 100:
    print('*', end='')
print()




#trường hợp cho ra 100 dấu ( * )
a = 0
while a < 100:
    print('*', end='')
    a += 1   # tăng a lên để thoát vòng lặp
print()


